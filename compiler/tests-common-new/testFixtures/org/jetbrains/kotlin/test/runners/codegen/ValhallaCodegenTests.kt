/**
 * Sets up the fixed Project Valhalla environment shared by the Valhalla smoke test runners: the same default directives as any
 * Valhalla codegen test (see [configureValhallaDefaultDirectives]), plus the runner-specific handling — tests that do not fit this
 * fixed environment (including the absence of a Valhalla JDK) are ignored by [ValhallaIncompatibleTestSkipper], and the dump
 * checks, which have no Valhalla-specific golden data, are suppressed by [ValhallaDumpChecksSuppressor].
 */
private fun TestConfigurationBuilder.configureValhallaSmokeTests() {
    defaultDirectives {
        configureValhallaDefaultDirectives()
    }
    useMetaTestConfigurators(::ValhallaIncompatibleTestSkipper)
    useFailureSuppressors(::ValhallaDumpChecksSuppressor)
}

/**
 * Ignores tests that don't fit the fixed Valhalla environment set up by [configureValhallaSmokeTests].
 *
 * Ignored unconditionally when the Valhalla JDK is not provided via the `JDK_VALHALLA` property.
 *
 * Tests that conflict with the pinned configuration:
 * - tests explicitly opted out via the `IGNORE_VALHALLA` directive (e.g. tests relying on behaviour that legitimately differs on a
 *   real Valhalla JDK, such as boxed-primitive identity);
 * - tests that specify their own JDK (via the `FULL_JDK` or `JDK_KIND` directive), because the Valhalla runners force the
 *   [TestJdkKind.FULL_JDK_VALHALLA] JDK globally, which would otherwise conflict with the JDK requested by the test itself;
 * - tests that specify their own JVM target / JDK release (via a `JVM_TARGET` other than [VALHALLA_JVM_TARGET], or a `JDK_RELEASE`
 *   directive).
 *
 * Tests incompatible with running the box function in a separate JVM process (required by [TestJdkKind.FULL_JDK_VALHALLA], which
 * launches the process via a `main` generated by
 * [org.jetbrains.kotlin.test.services.sourceProviders.MainFunctionForBlackBoxTestsSourceProvider]):
 * - tests using `USE_LEGACY_REFLECTION_IMPLEMENTATION`, which is explicitly unsupported in a separate process;
 * - multiplatform tests: the launcher `main` is generated next to `box()`, but when `box()` lives in a non-JVM module (e.g. the
 *   `common` module) the box is executed from the leaf module's artifact, where the launcher cannot be found.
 */
private class ValhallaIncompatibleTestSkipper(testServices: TestServices) : MetaTestConfigurator(testServices) {
    override fun shouldSkipTest(): Boolean {
        val directives = testServices.moduleStructure.allDirectives
        return !KtTestUtil.isJdkValhallaAvailable() ||
                IGNORE_VALHALLA in directives ||
                FULL_JDK in directives ||
                directives[JDK_KIND].any { it != TestJdkKind.FULL_JDK_VALHALLA } ||
                directives[JVM_TARGET].any { it != VALHALLA_JVM_TARGET } ||
                JDK_RELEASE in directives ||
                USE_LEGACY_REFLECTION_IMPLEMENTATION in directives ||
                directives[LANGUAGE].any { "MultiPlatformProjects" in it }
    }
}

/**
 * Suppresses failures coming from the IR text ([IrTextDumpHandler]), bytecode listing ([BytecodeListingHandler]) and bytecode text
 * ([BytecodeTextHandler]) dump checks: their golden data was produced for the default configuration and does not match the Valhalla
 * one (different JVM target, preview features enabled). The runtime behaviour of the box tests is still verified.
 */
private class ValhallaDumpChecksSuppressor(testServices: TestServices) : TestFailureSuppressor(testServices) {
    override fun suppressIfNeeded(failedAssertions: List<WrappedException>): List<WrappedException> {
        return failedAssertions.filterNot {
            it is WrappedException.FromHandler &&
                    (it.handler is IrTextDumpHandler || it.handler is BytecodeListingHandler || it.handler is BytecodeTextHandler)
        }
    }

    override fun checkIfTestShouldBeUnmuted() {}
}

/**
 * Verifies that Black Box tests are run successfully against the Valhalla JDK without further additional checks.
 */
open class AbstractValhallaBlackBoxSmokeTest : AbstractJvmBlackBoxCodegenTestBase(FirParser.LightTree) {
    override fun configure(builder: TestConfigurationBuilder) {
        super.configure(builder)
        builder.configureValhallaSmokeTests()
    }
}
